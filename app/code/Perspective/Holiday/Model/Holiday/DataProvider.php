<?php

namespace Perspective\Holiday\Model\Holiday;

use Perspective\Holiday\Model\ResourceModel\Holiday\CollectionFactory;
use Magento\Framework\App\Request\DataPersistorInterface;
use Magento\Ui\DataProvider\Modifier\PoolInterface;

/**
 * Class DataProvider
 */
class DataProvider extends \Magento\Ui\DataProvider\ModifierPoolDataProvider
{
    /**
     * @var \Perspective\Holiday\Model\ResourceModel\Holiday\Collection
     */
    protected $collection;

    /**
     * @var DataPersistorInterface
     */
    protected $dataPersistor;

    /**
     * @var array
     */
    protected $loadedData;

    /**
     * Constructor
     *
     * @param string $name
     * @param string $primaryFieldName
     * @param string $requestFieldName
     * @param CollectionFactory $blockCollectionFactory
     * @param DataPersistorInterface $dataPersistor
     * @param array $meta
     * @param array $data
     * @param PoolInterface|null $pool
     */
    public function __construct(
        $name,
        $primaryFieldName,
        $requestFieldName,
        CollectionFactory $collectionFactory,
        DataPersistorInterface $dataPersistor,
        array $meta = [],
        array $data = [],
        PoolInterface $pool = null
    ) {
        $this->collection = $collectionFactory->create();
        $this->dataPersistor = $dataPersistor;
        parent::__construct($name, $primaryFieldName, $requestFieldName, $meta, $data, $pool);
    }

    /**
     * Get data
     *
     * @return array
     */
    public function getData()
    {
        if (isset($this->loadedData)) {
            return $this->loadedData;
        }
        $items = $this->collection->getItems();
        /** @var \Perspective\Holiday\Model\Holiday $holiday */
        foreach ($items as $holiday) {
            $this->loadedData[$holiday->getId()] = $holiday->getData();
        }

        $data = $this->dataPersistor->get('perspective_holiday_days');
        if (!empty($data)) {
            $holiday = $this->collection->getNewEmptyItem();
            $holiday->setData($data);
            $this->loadedData[$holiday->getId()] = $holiday->getData();
            $this->dataPersistor->clear('perspective_holiday_days');
        }

        return $this->loadedData;
    }
}
